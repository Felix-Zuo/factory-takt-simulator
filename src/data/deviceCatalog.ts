export const createDefaultPortRule = (
  side: 'input' | 'output',
  index: number,
  materialFilter: PortMaterialFilter = 'mixed',
): PortRule => ({
  enabled: true,
  label: `${side === 'input' ? 'IN' : 'OUT'}${index}`,
  materialFilter,
  routingStrategy: side === 'output' ? 'round_robin' : 'lowest_inventory_first',
  blockedBehavior: side === 'output' ? 'skip_blocked' : 'wait_blocked',
  minBatch: 1,
  maxBatch: 9999,
  priority: index,
  allocationRatio: 1,
});

export const createDefaultPortRules = (
  side: 'input' | 'output',
  count: number,
  materialFilter: PortMaterialFilter = 'mixed',
): Record<string, PortRule> =>
  Object.fromEntries(
    Array.from({ length: Math.max(1, Math.min(4, count)) }, (_, index) => {
      const handleId = `${side === 'input' ? 'in' : 'out'}-${index + 1}`;
      return [handleId, createDefaultPortRule(side, index + 1, materialFilter)];
    }),
  );

