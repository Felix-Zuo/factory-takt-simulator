export const createNodeData = (type: DeviceType, index: number): DeviceNodeData => {
  const catalogItem = getCatalogItem(type);
  const params = {
    ...catalogItem.defaultParams,
    deviceCode: `${catalogItem.shortName.replace(/\s+/g, '-').toUpperCase()}-${String(index).padStart(2, '0')}`,
  };

  return {
    label: catalogItem.shortName,
    params,
    metrics: createEmptyMetrics(params),
    runtime: createRuntime(params.enabled ? 'idle' : 'stopped'),
  };
};
